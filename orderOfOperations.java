public class orderOfOperations {
    public static void main(String[] args) {
        int x = 10 + 3 * 2;
        // BIDMAS - Brackets, Indices, Division, Multiplication, Addition, Subtraction
        // 3 * 2 = 6
        // so x = 10 + ( 3 * 2 )
        // or 10 + 6
        // so x = 16
        System.out.println(x);
    }
}
